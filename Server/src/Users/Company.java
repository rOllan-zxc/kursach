package Users;

public class Company {
    String nameCompany;
    double cahs;

    public void setNameCompany(String nameCompany) {
        this.nameCompany = nameCompany;
    }

    public void setCahs(double cahs) {
        this.cahs = cahs;
    }

    public String getNameCompany() {
        return nameCompany;
    }

    public double getCahs() {
        return cahs;
    }
}
